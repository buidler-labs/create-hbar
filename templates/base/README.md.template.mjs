import { upperCaseFirstLetter, withDefaults } from "../utils.js";

const getQuickStart = ({
  solidityFramework,
  networkConfigPath,
  contractsPath,
  scriptsPath,
  testCommand,
}) => `
## Quickstart

To get started with your Hedera dApp, follow the steps below:

1. Install dependencies if it was skipped in CLI:

\`\`\`
yarn install
\`\`\`
${
  Boolean(solidityFramework[0])
    ? `
2. Run a local network in the first terminal:

\`\`\`
yarn chain
\`\`\`

This starts a local network using ${upperCaseFirstLetter(solidityFramework[0])}. You can customize the network in ${networkConfigPath[0]}.

3. On a second terminal, deploy the contracts:

\`\`\`
yarn deploy
\`\`\`

Contracts live in ${contractsPath[0]}; deploy scripts are in ${scriptsPath[0]}.

4. On a third terminal, start your Next.js app:`
    : "2. Start your Next.js app:"
}

\`\`\`
yarn start
\`\`\`

Open \`http://localhost:3000\` and use the **Debug Contracts** page to interact with your contracts. App config: \`packages/nextjs/scaffold.config.ts\`.
${
  Boolean(solidityFramework[0])
    ? `
5. **Deploy and verify on Hedera:** To deploy or verify on Hedera testnet, you need a deployer account with testnet HBAR. Generate an account with \`yarn generate\` or \`yarn account:import\` (in the contract package), then fund it using the [Hedera Portal faucet](https://portal.hedera.com/faucet). After the account has funds, run \`yarn deploy --network hederaTestnet\` (Hardhat) or \`yarn deploy --network hedera_testnet\` (Foundry). After deploy, verify on [Hashscan](https://hashscan.io) with \`yarn verify:testnet\` (or \`yarn verify:mainnet\` for mainnet). No extra arguments are needed for verify; it uses the last deployment artifacts.

Run tests with ${testCommand[0]}

- Edit contracts in ${contractsPath[0]}
- Edit the frontend in \`packages/nextjs/app/page.tsx\` and deployment scripts in ${scriptsPath[0]}
`
    : ""
}`;

const contents = ({
  skipQuickStart,
  solidityFramework,
  networkConfigPath,
  contractsPath,
  scriptsPath,
  testCommand,
  extraContents,
  fullContentOverride,
}) => {

  if (fullContentOverride[0]) {
    return `${fullContentOverride[0]}

Generated by [create-hbar](https://github.com/hashgraph/create-hbar)`;
  }

  return `# Hedera dApp

<h4 align="center">
  <a href="https://docs.hedera.com">Hedera Docs</a> |
  <a href="https://hashscan.io">Hashscan</a> |
  <a href="https://github.com/hashgraph/create-hbar">create-hbar</a>
</h4>

A Hedera-ready dApp scaffold with Next.js, ${
    Boolean(solidityFramework[0]) ? upperCaseFirstLetter(solidityFramework[0]) + ", " : ""
  }RainbowKit, Wagmi, and Viem.

${
  Boolean(solidityFramework[0])
    ? "\n- **Contract Hot Reload**: Frontend updates as you edit contracts."
    : ""
}
- **Custom hooks** and **components** for contract interaction with TypeScript.
- **Burner Wallet & Local Faucet** for local testing.
- **Wallet integration** for Hedera testnet and mainnet.

## Requirements

- [Node (>= v20.18.3)](https://nodejs.org/en/download/)
- [Yarn](https://yarnpkg.com/getting-started/install) (v1 or v2+)
- [Git](https://git-scm.com/downloads)
${skipQuickStart[0] ? "" : getQuickStart({
  solidityFramework,
  networkConfigPath,
  contractsPath,
  scriptsPath,
  testCommand,
})}
${extraContents.filter(Boolean).join("\n")}
## Links

- [Hedera Documentation](https://docs.hedera.com)
- [Hashscan](https://hashscan.io) — block explorer
- [create-hbar](https://github.com/hashgraph/create-hbar) — scaffold more Hedera dApps`;
}

export default withDefaults(contents, {
  skipQuickStart: false,
  networkConfigPath: "",
  contractsPath: "",
  scriptsPath: "",
  testCommand: "",
  extraContents: "",
  fullContentOverride: "",
});
